create or replace function public.kp_hauling_is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.kp_hauling_profiles
    where id = auth.uid()
      and role in ('owner', 'admin')
  );
$$;

