where email <> 'admin@kp.local';

insert into public.kp_hauling_profiles (id, name, email, role, availability_status, availability_updated_at)
select id, 'KP Owner', email, 'owner', 'Available', now()
from auth.users
where lower(email) = 'admin@kp.local'
on conflict (id) do update set
  name = excluded.name,
  email = excluded.email,
  role = excluded.role,
  availability_status = excluded.availability_status,
  availability_updated_at = excluded.availability_updated_at,
  updated_at = now();
