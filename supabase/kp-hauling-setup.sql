create table if not exists public.kp_hauling_profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  name text not null,
  email text not null unique,
  role public.kp_hauling_user_role not null default 'driver',
  phone text,
  availability_status public.kp_hauling_availability_status not null default 'Unavailable',
  availability_notes text,
  availability_updated_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.kp_hauling_driver_availability_windows (
  id uuid primary key default gen_random_uuid(),
  driver_id uuid not null references public.kp_hauling_profiles(id) on delete cascade,
  available_date date not null,
  start_time time not null,
  end_time time not null,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint kp_hauling_availability_valid_time check (start_time < end_time)
);

create table if not exists public.kp_hauling_dumpsters (
  id uuid primary key default gen_random_uuid(),
  number text not null unique,
  size public.kp_hauling_dumpster_size not null,
  type public.kp_hauling_dumpster_type not null default 'Roll-off',
  status public.kp_hauling_dumpster_status not null default 'Available',
  current_location text not null default 'KP yard',
  current_address text not null default '',
  current_customer text,
  current_job_id uuid,
  notes text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

